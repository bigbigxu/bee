    /**
     * 从cache查找所有数据
     * 可以根据分值进行查找
     * @param array $query
     * @return array
     * @throws Exception
     */
    public function cacheAllByScore($query = array())
    {
        $this->redisQuery = array_merge($this->redisQuery, $query);
        $indexKey = $this->getIndexKey();
        $min = $this->redisQuery['min_score'];
        $max = $this->redisQuery['max_score'];
        $limit = $this->redisQuery['limit'];
        $sort = $this->redisQuery['sort'];

        if ($sort == self::SORT_DESC) {
            $idArr = $this->redis()->zRevRangeByScore($indexKey, $max, $min, array('limit' => $limit));
        } else {
            $idArr = $this->redis()->zRangeByScore($indexKey, $min, $max, array('limit' => $limit));
        }
        $r = $this->getDataById($idArr);
        $this->clearQuery();
        return $r;
    }

    /**
     * 根据排名查找数据
     * @param array $query
     * @return array
     * @throws Exception
     */
    public function cacheAllByRank($query = array())
    {
        $this->redisQuery = array_merge($this->redisQuery, $query);
        $indexKey = $this->getIndexKey();
        $start = $this->redisQuery['min_rank'];
        $end = $this->redisQuery['max_rank'];
        $sort = $this->redisQuery['sort'];
        $limit = $this->redisQuery['limit'];
        if ($sort == self::SORT_ASC) {
            $idArr = $this->redis()->zRange($indexKey, $start, $end);
        } else {
            $idArr = $this->redis()->zRevRange($indexKey, $start, $end);
        }
        if ($limit) { //此处只能使用数组分页
            $idArr = array_slice($idArr, $limit[0], $limit[1]);
        }
        $r = $this->getDataById($idArr);
        $this->clearQuery();
        return $r;
    }

    /**
     * 根据ID进行查找数据
     * @TODO 此方法没有测试
     * @param array $query
     * @return array
     * @throws Exception
     */
    public function cacheAllById($query = array())
    {
        $this->redisQuery = array_merge($this->redisQuery, $query);
        $indexKey = $this->getIndexKey();
        $min = $this->redisQuery['min_id'];
        $max = $this->redisQuery['max_id'];
        $sort = $this->redisQuery['sort'];
        $limit = $this->redisQuery['limit'];

        if ($sort == self::SORT_ASC) {
            $start = $this->redis()->zRank($indexKey, $min);
            $end = $this->redis()->zRank($indexKey, $max);
        } else {
            $start = $this->redis()->zRevRank($indexKey, $min);
            $end = $this->redis()->zRevRank($indexKey, $max);
        }
        $start = $start ? $start : 0;
        $end = $end ? $end : -1;
        $rank1 = min($start, $end);
        $rank2 = $end == -1 ? $end : max($start, $end);

        if ($sort == self::SORT_ASC) {
            $idArr = $this->redis()->zRange($indexKey, $rank1, $rank2);
        } else {
            $idArr = $this->redis()->zRevRange($indexKey, $rank1, $rank2);
        }
        if ($limit) { //此处只能使用数组分页
            $idArr = array_slice($idArr, $limit[0], $limit[1]);
        }
        $r = $this->getDataById($idArr);
        $this->clearQuery();
        return $r;
    }

    /**
     * 根据ID得到详情
     * @param $idArr
     * @return array
     */
    public function getDataById($idArr)
    {
        $r = array();
        foreach ((array)$idArr as $id) {
            if ($id == false) {
                continue;
            }
            $item = $this->cacheOne($id);
            if ($this->redisQuery['field']) {
                $item = Functions::arrayFilterKey($item, $this->redisQuery['field']);
            }
            $r[] = $item;
        }
        return $r;
    }

    /**
     * 检查索引
     * @return string 索引的key
     * @throws Exception
     */
    private function _checkCacheIndex()
    {
        if ($this->redisQuery['index'] && in_array($this->redisQuery['index'], $this->indexField())) {
            $indexKey = $this->cacheIndexKey($this->redisQuery['index']);
        } elseif ($this->redisQuery['group'] && array_key_exists($this->redisQuery['group'][0], $this->groupField())) {
            $indexKey = $this->cacheGroupKey($this->redisQuery['group'][0], $this->redisQuery['group'][1]);
        } else {
            throw new Exception('未定义的索引或分组');
        }

        //是否自动重建索引
        if ($this->redis()->exists($indexKey) == false) {
            if ($this->autoCreateIndex) {
                if ($this->redisQuery['index']) {
                    $this->buildIndex($this->redisQuery['index']);
                } elseif ($this->redisQuery['group']) {
                    $this->buildGroup($this->redisQuery['group'][0],  $this->redisQuery['group'][1]);
                }
            } else {
                throw new Exception("索引或分组不存在");
            }
        }
        return $this->redisQuery['index_key'] = $indexKey;
    }
    /**
     * 一次查询执行后，清量相关参数
     */
    public function clearQuery()
    {
        $this->lastQuery = $this->redisQuery;
        $this->redisQuery = array(
            'field' => '', //查询的字段，数组形式
            'sort' => self::SORT_ASC, //排序类型
            'limit' => array(), //开始位置
            'min_score' => '-inf', //索引最小值
            'max_score' => '+inf', //索引最大值
            'index' => '',
            'group' => '',
            'min_rank' => 0, //最小排名
            'max_rank' => -1, //最大排名
            'min_id' => 0, //最小ID
            'max_id' => 0, //最大ID
            'cache_key' => ''
        );
    }

    /**
     * 创建指定字段的索引
     * @param string $field 要创建索引的字段
     * @param PDOStatement()|null $stmt 一个stmt对象。如果为空，将遍历整个表。
     */
    public function buildIndex($field, $stmt = null)
    {
        if ($stmt === null) {
            $stmt = $this->from()->query();
        }
        $indexKey = $this->cacheIndexKey($field);
        $pkName = $this->getPkName();
        foreach ($stmt as $row) {
            $this->redis()->zAdd($indexKey, $row[$field], $row[$pkName]);
        }
        $this->redis()->expire($indexKey, $this->indexExpire);
    }

    /**
     * 创建分组索引
     * @param $field
     * @param $value
     */
    public function buildGroup($field, $value)
    {
        $groupField = $this->groupField();
        $sortField = $groupField[$field];

        $stmt = $this->from()->andFilter($field, '=', $value)->all();
        $pkName = $this->getPkName();
        foreach ($stmt as $row) {
            $groupKey = $this->cacheGroupKey($field, $row[$field]);
            $this->redis()->zAdd($groupKey, $row[$sortField], $row[$pkName]);
            $this->redis()->expire($groupKey, $this->indexExpire);
        }
    }

    /**
     * 设置选项。
     * @param $key
     * @param $value
     * @return $this
     */
    public function setOption($key, $value)
    {
        $vars = get_class_vars($this);
        if (in_array($key, $vars)) {
            $this->$key = $value;
        }
        return $this;
    }

    /**
     * 设置多个选项
     * @param $options
     * @return $this
     */
    public function setOptions($options)
    {
        foreach ($options as $key => $value) {
            $this->setOption($key, $value);
        }
        return $this;
    }

    /**
     * 设置索引cache的前缀
     * @param $key
     * @return $this
     */
    public function setIndexKeyPrefix($key)
    {
        $this->indexKeyPrefix = $key;
        return $this;
    }

    /**
     * 设置cli命令执行路径
     * @param $cli
     * @return $this
     */
    public function setRedisCli($cli)
    {
        $this->redisCli = $cli;
        return $this;
    }

    /**
     * 删除缓存的索引
     * @return string
     */
    public function delCacheIndex()
    {
        $params = func_get_args();
        if (count($params) == 0) {
            return false;
        }
        foreach ($params as $field) {
            $key = $this->cacheIndexKey($field);
            $this->redis()->del($key);
        }
        return true;
    }

    /**
     * 删除一个分组key
     * @param $field
     * @param $value
     */
    public function delCacheGroup($field, $value)
    {
        $key = $this->cacheGroupKey($field, $value);
        $this->redis()->delKeysCmd($key);
    }

    /**
     * 删除所有数据。
     */
    public function delCacheData()
    {
        $keys = "{$this->redisKeyPrefix}{$this->indexKeyPrefix}*";
        $cmd = $this->redis()->delKeysCmd($keys, $this->redisCli);
        echo shell_exec($cmd);
    }

    public function errorLog($msg)
    {
        CoreLog::error($msg);
    }

    /**
     * 删除所有数据
     */
    public function delAll()
    {
        if ($this->redisKeyPrefix == false) {
            throw new Exception('cache前缀不能为空');
        }
        $keys = "{$this->redisKeyPrefix}*";
        $cmd = $this->redis()->delKeysCmd($keys, $this->redisCli);
        echo shell_exec($cmd);
    }

    /**
     * 返回当前设置查询的index_key
     * @return mixed
     * @throws Exception
     */
    public function getIndexKey()
    {
        $key =  $this->redisQuery['index_key'];
        if ($key == false) {
            throw new Exception('请设置查询key');
        }
        return $key;
    }
