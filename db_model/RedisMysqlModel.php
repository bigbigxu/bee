    /**
     * 根据主键查找一条记录
     * @param $pk
     * @param bool|true $checkCache
     * @return array|bool|false|string
     */
    public function cacheOne($pk, $checkCache = true)
    {
        $r = false;
        if ($checkCache) {
            $r = $this->redis()->hGetAll($this->cacheDataKey($pk));
        }
        if ($r == false) { //如果cache不存在
            $r = $this->_cache($pk, array(), self::OP_FIND);
        }
        return $r;
    }

